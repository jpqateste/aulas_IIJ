'''Fazer um programa que some 4 notas e, no final, tenha a média aritmética dessas notas.
'''
n1 = int(input("Digite sua nota 1: "))
n2 = int(input("Digite sua nota 2: "))
n3 = int(input("Digite sua nota 3: "))
n4 = int(input("Digite sua nota 4: "))

media = (n1 + n2 + n3 + n4) / 4
print(f"Olá, Clislania! Sua média é: {media} pontos")